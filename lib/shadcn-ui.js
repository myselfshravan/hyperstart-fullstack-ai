import path from 'path';

export const setupShadcnUI = (projectPath, selectedComponents = [], framework = 'vite') => {
    if (!selectedComponents || selectedComponents.length === 0) return;

    installShadcnUI(projectPath);
    setupTailwindConfig(projectPath, framework);
    createComponentsConfig(projectPath, framework);
    createUtilsFile(projectPath, framework);
    
    selectedComponents.forEach(component => {
        installShadcnComponent(projectPath, component);
    });
};

